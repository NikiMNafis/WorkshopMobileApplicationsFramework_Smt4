void main() {
  // Operator Aritmatika
  print(5 + 5); //tambah
  print(5 - 3); //kurang
  print(4 * 3); //kali
  print(8 / 2); //bagi
  print(13 % 2); //modulus
  // Operator Assignment
  var nama;
  nama = "Niki M Nafis";
  print(nama);
  // Operator Perbandingan
  var nilai = 100; //equal
  print(nilai == 90);
  print(nilai == 100);
  var warna = "hitam"; //not equal
  print(warna != "merah");
  print(warna != "biru");
  var angka = 12; //strict equal
  // print(angka === "12");
  // print(angka === 12);
  var nilai2 = 75; //kurang dari & lebih dari
  print(nilai2 < 80);
  print(nilai2 > 80);
  print(nilai2 >= 75);
  print(nilai2 <= 70);
  // Operator Kondisional
  print(true && true); // AND
  print(true && false);
  print(false && false);
  print(false && true && true);
  print(true && true && true);
  print(true || true); // OR
  print(true || false);
  print(true || false || false);
  print(false || false);
}
